# Podemos agregar un solo elemento a una lista usando .append ()

empty_list = []

empty_list.append(1)

print(empty_list) # [1]

# Create a list
my_list = [1, 2, 3]

# Append a number
my_list.append(5)
print(my_list) # check the result [1, 2, 3, 5]

