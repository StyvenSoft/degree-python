fruits = ['apple', 'banana', 'cherry', 'date']
print(fruits[0:3])
# ['apple', 'banana', 'cherry']

print(fruits[:3])
# ['apple', 'banana', 'cherry']

print(fruits[2:])
# ['cherry' , 'date']

print(fruits[-3:])
# ['banana', 'cherry', 'date']



